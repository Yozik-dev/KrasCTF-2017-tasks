int main() {
  boost::asio::io_service io_service;
  boost::asio::signal_set signals(io_service);
  signals.add(SIGINT);
  signals.add(SIGTERM);
  signals.async_wait([&io_service](boost::system::error_code, int) {
    io_service.stop();
  });
  RunHTTPServer(io_service, 8080);
}
